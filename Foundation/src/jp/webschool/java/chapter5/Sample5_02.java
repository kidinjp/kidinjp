package jp.webschool.java.chapter5;

public class Sample5_02 {
	/**
	 * 「if - else 文」の確認 5_04
	 * @param args
	 */
	public static void main(String[] args) {
		// 変数を宣言
		int a = 5;
		// 「if - else 文」の確認
		if (a >= 5) {
			System.out.println("5以上です");
		} else {
			System.out.println("5未満です");
		}
	}

}
