package jp.webschool.java.chapter3;

public class Sample3_03 {

	public static void main(String[] args) {
		// 変数の使い方（変数の値を変える）3_08
		// 変数 a を 10 で初期化
		int a = 10;
		System.out.println(a);
		// 変数 a に 20 を代入
		a = 20;
		System.out.println(a);
		// 変数 a に 120 を代入
		a = 120;
		System.out.println(a);
	}
}