package jp.webschool.java.chapter4;

public class Sample4_07 {
	/**
	 * 小さなサイズの型の変数に代入 4_11
	 * @param args
	 */
	public static void main(String[] args) {
		// double 型
		double dnum = 30.48;
		System.out.println("1 フィートは約 " + dnum + " センチメートルです");
		// int 型
		int inum = (int)dnum;
		System.out.println("1 フィートは約 " + inum + " センチメートルです");
		
	}

}
