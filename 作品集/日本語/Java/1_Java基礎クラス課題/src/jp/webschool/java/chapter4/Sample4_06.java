package jp.webschool.java.chapter4;

public class Sample4_06 {
	/**
	 * 大きなサイズの型の変数に代入
	 * @param args
	 */
	public static void main(String[] args) {
		// int 型
		int inum = 30;
		System.out.println("1 フィールドは約 " + inum + " センチメートルです");
		// double 型
		double dnum = inum;
		System.out.println("1 フィールドは約 " + dnum + " センチメートルです");
		

	}

}
